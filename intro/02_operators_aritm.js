// operadores aritmeticos básicos
let counter = 100;

// 1. suma
counter = counter + 20; // 120
console.log(counter);
counter += 20; // 140
console.log(counter);

// resta
let age = 20;
age = age - 2; // forma larga
age -= 2; // forma corta

// multiplicacion
let numberA = 5;
numberA = numberA * 10;
numberA *= 10;

// división
let numberB = 20;
numberB = numberB / 5;
numberB /= 5;

// modulo / residuo
let numberC = 21;
numberC = numberC % 5; // 1
numberC %= 5;

// exponenciación
let numberD = 10;
numberD = numberD ** 2; // 100
numberD **= 2; 
